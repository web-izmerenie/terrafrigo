<p><strong>Как правильно подобрать автомобильный рефрижератор?</strong></p>
<p>Допустим, необходимо рефрижераторное оборудование на Газель с объемом фургона 15 м<sup>3</sup>, продукт, который следует перевозить &mdash; замороженные овощи, фрукты и ягоды, необходимая температура при этом должна быть -17 С&deg;.  В данном случае мы может порекомендовать рефрижератор серии S30, который поддерживает температурный режим в фургоне от 0С&deg; до -20С&deg;, объем до 18 м<sup>3</sup>.</p>
