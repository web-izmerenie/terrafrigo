<p>
	 Владельцы коммерческого транспорта и сельхозтехники могут выбрать и купить один из двух вариантов исполнения кондиционера, который наиболее необходим.
</p>
<p>
 <strong>1. Установка штатных автокондиционеров.</strong>
</p>
<p>
	 Конструкция испарителя монтируется в штатную систему отопления при этом компрессор автокондиционера устанавливается на двигателе, а конденсатор - перед штатным радиатором системы охлаждения:
</p>
<ul>
	<li>Эргономичный, не требует дополнительного свободного места в салоне, тк устанавливается в штатную систему отопления автомобиля.</li>
	<li>Оптимальная мощность. Подходит для кабин грузовых или легковых автомобилей, вместимостью не более 5 человек.</li>
	<li>Устанавливается на таких автомобилях как: УАЗ Патриот, Нива Шевроле, Газель Бизнес, Газель Некст, Fiat Ducato Sollers, Fiat Ducato New, Mitsubishi Fuso, Hyundai HD 78, Hyundai HD 120, Hyundai HD 170, Камаз Isuzu NQR.</li>
</ul>
<p>
 <strong>2. Установка крышных автокондиционеров.</strong>
</p>
<p>
	 На крыше автомобиля устанавливается конденсаторный блок. Мощные крышные кондиционеры позволяют быстро охладить салон коммерческого транспорта:
</p>
<ul>
