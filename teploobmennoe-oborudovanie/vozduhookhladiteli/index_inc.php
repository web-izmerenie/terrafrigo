</p>
<p>
	Воздухоохладители делятся на несколько видов. Исходя из конструктивных признаков, воздухоохладитель бывают угловыми, кубическими, потолочными, двухпоточными, оснащенными центробежными или осевыми вентиляторами. Что касается области применения, выделяют промышленные и коммерческие серии воздухоохладителей.
</p>
