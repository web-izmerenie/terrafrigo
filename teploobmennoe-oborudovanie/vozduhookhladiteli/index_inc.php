<h2>Воздухоохладители</h2>
<br>
<p>
	Воздухоохладители – специальные теплообменные аппараты, которые обеспечивают охлаждение воздуха в специальных холодильных помещениях по средством отвода тепла из внешних источников. В процессе охлаждения высокотемпературных тел, холодильная машина забирает холод у низкотемпературных тел. Предназначенные для небольших холодильных камер, с рабочей температурой от +10 до -18 С.
</p>
<p>
	Воздухоохладители применяются в различных установках:
</p>
<ul>
	<li>камеры шоковой заморозки;</li>
	<li><a href="/">холодильное оборудование</a> предприятий;</li>
	<li>холодильное оборудование для складов;</li>
	<li>холодильные шкафы в супермаркетах, на торговых предприятиях.</li>
</ul>
<p>
