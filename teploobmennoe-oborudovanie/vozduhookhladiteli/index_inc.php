</p>
<p>
	Вентиляторы оборудованы однофазным электродвигателем с питанием от сети 220 В, 50 гц. Степень защиты IP44. Вентиляторы засасывают воздух из камеры воздухоохладителя, прогоняет его через встроенную охлаждающую батарею, подают обратно в камеру. В зависимости от модели, в воздухоохладителе может использоваться от одного до трех вентиляторов.
</p>
<p>
	Номинальная хладопроизводительность воздухоохладителя рассчитывается в соответствии с правила Evrovent и ENV328.
</p>
<p>
