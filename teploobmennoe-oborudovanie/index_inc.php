<p>
	Теплообменное оборудование (теплообменники) – это специальные конструкции, которые позволяют передавать тепловую энергию от одного носителя тепла к другому, причем теплоноситель может быть как жидким, так и газообразным.
</p>
<p>
	<strong>Теплообменное оборудование (теплообменники) используют в:</strong>
</p>
<ul>
	<li>отопительном оборудовании;</li>
	<li>промышленной холодильной технике;</li>
	<li>автомобильной промышленности;</li>
	<li>вентиляции, кондиционировании воздуха;</li>
	<li>оборудовании, в котором используется система нагрева, охлаждения (воды, тосола, масла воздухом), осушения воздуха.</li>
</ul>
