</p>
<p>
	<strong>Стандартный компрессорно-конденсаторный блок состоит из:</strong>
</p>
<ul>
	<li>компрессора, который «сжимает» фреон и способствует его циркуляции;</li>
	<li>конденсатора (теплообменника), в котором происходит конденсация фреона, в результате чего выделяется тепло;</li>
	<li>вентилятора, обдувающего конденсатор и улучшающего отвод тепла от блока.</li>
</ul>
