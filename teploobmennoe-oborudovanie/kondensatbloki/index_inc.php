<h1>Компрессорно-конденсаторные блоки</h1>
<br>
<p>
	<b>Компрессорно-конденсаторный блок - </b>это оборудование, предназначенное для подготовки жидкого хладагента и последующей его подачи в теплообменник внутреннего блока климатической техники.
</p>
<p>
