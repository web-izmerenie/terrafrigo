<h1>Калориферы&nbsp;</h1>
<br>
<p>
	Калорифер – это устройство для подогрева воздуха, подаваемого в прохладное время года с улицы в помещение. Приходящий в калорифер поток воздуха нагревается в процессе прохождения через теплообменник, и поступает в необходимое помещение.
</p>
<p>
	По принципу работы различают: водяные калориферы, которые подключаются к центральному отоплению, и электрические, работающие от сети. Завод ТерраФриго производит водяные калориферы.
</p>
<p>
	Калориферы устанавливаются в вентиляционной системе. Это может быть отдельный модуль или моноблочная вентиляционная установка. Электрические калориферы целесообразно применять в небольших помещениях площадью до 150 кв.м., а водяные калориферы прекрасно подойдут для больших складов, магазинов, офисов с площадью более 150 кв.м.
</p>
