<p>
	<em>Установка водяных калориферов</em>
</p>
<p>
	Работу системы водяного калорифера контролирует капиллярный термостат, который отслеживает температуру обратной воды и наружного воздуха. Если показатели опускаются ниже заданного уровня, установка отключается. Приточные установки с водяным калорифером предусматривают защиту от обмерзания воздуха.
</p>
<p>
	Стандартная установка калорифера водяного предусматривает работу электрического привода, обеспечивающего автоматическое закрывание и открывание воздушного клапана в водяном калорифере. Возвратная пружина позволяет предотвратить замерзание, если произойдет отключение электроэнергии.
</p>
